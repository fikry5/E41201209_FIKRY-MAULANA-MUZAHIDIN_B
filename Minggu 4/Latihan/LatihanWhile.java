public class LatihanWhile {
    public static void main(String[] args) {
        int i = 0;
        
        while (i <= 10) {
            // blok kode yang akan diulang
            System.out.println("Perulangan ke-" + i);
            // increment nilai i
            i++;
        }
    }
}
